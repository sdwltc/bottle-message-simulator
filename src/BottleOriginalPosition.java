public class BottleOriginalPosition
{
    private int row;
    private int col;

    public BottleOriginalPosition(int row, int col)
    {
        this.row = row;
        this.col = col;
    }

    public int getOrgnRow()
    {
        return row;
    }

    public int getOrgnCol()
    {
        return col;
    }
}
